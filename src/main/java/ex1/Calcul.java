package ex1;

/**
 * La classe Calcul fournit des opérations mathématiques de base telles que l'addition et la soustraction.
 */
public class Calcul {

	/**
	 * Calcule la somme de deux entiers.
	 *
	 * @param a le premier entier
	 * @param b le deuxième entier
	 * @return la somme de a et b
	 */
	public int additionner(int a, int b) {
		return a + b;
	}

	/**
	 * Calcule la différence entre deux entiers.
	 *
	 * @param a le premier entier
	 * @param b le deuxième entier
	 * @return la différence entre a et b
	 */
	public int soustraire(int a, int b) {
		return a - b;
	}
}
